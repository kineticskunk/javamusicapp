package com.company.model;

public class Song {
    private int id;
    private int track;
    private String name;
    private int albumId;

    public int getId() {
        return id;
    }

    public int getTrack() {
        return track;
    }

    public String getName() {
        return name;
    }

    public int getAlbumId() {
        return albumId;
    }
}
